#include "../../include/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void			*ptr;
	unsigned char	*d;

	if (!(ptr = malloc(size * count)))
		return (NULL);
	d = ptr;
	count *= size;
	while (count)
	{
		*d = 0;
		d++;
		count--;
	}
	return (ptr);
}
