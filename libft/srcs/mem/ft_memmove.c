#include "../../include/libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	if (src == NULL && dst == NULL && len != 0)
		return (0);
	d = dst;
	s = src;
	if (src < dst)
	{
		while (len--)
			d[len] = s[len];
	}
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
